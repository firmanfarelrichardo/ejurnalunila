document.addEventListener('DOMContentLoaded', () => {
    const mobileMenuButton = document.getElementById('mobile-menu-toggle');
    const navWrapper = document.getElementById('nav-wrapper');

    if (mobileMenuButton && navWrapper) {
        mobileMenuButton.addEventListener('click', (event) => {
            event.stopPropagation(); // Mencegah event lain terpanggil
            navWrapper.classList.toggle('open');
        });
    }
});

document.addEventListener('DOMContentLoaded', () => {
    // Membuka modal
    document.querySelectorAll('[data-modal-target]').forEach(trigger => {
        trigger.addEventListener('click', event => {
            event.preventDefault();
            const modalId = trigger.getAttribute('data-modal-target');
            const modal = document.getElementById(modalId);
            if (modal) {
                modal.showModal();
            }
        });
    });

    // Menutup modal dengan tombol close
    document.querySelectorAll('.info-modal .close-button').forEach(closeButton => {
        closeButton.addEventListener('click', () => {
            const modal = closeButton.closest('.info-modal');
            modal.close();
        });
    });

    // Menutup modal dengan klik di luar area konten
    document.querySelectorAll('.info-modal').forEach(modal => {
        modal.addEventListener('click', event => {
            if (event.target === modal) {
                modal.close();
            }
        });
    });
});
</script>